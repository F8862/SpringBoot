package com.demo;

public class Calculator {
    //sum
    public int doSum(int a,int b,int c){
        return a+b+c;
    }
    //multiplication
    public int doProduct(int a,int b){
        return a*b;
    }

    //compare 2 numbers
    public  boolean compareTwoNumbers(int a,int b){
        return a==b;
    }


}
